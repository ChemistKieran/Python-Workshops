# There are two functions you need to know before we start:
#   - print() - prints the value of the argument passed to it
#   - type() - returns the type of the argument passed to it

# you also need to know about f-strings
# f-strings are a way to format strings in python
# you can use them to print variables inside of strings



# Integer
my_integer = 42
print(f"Integer: {my_integer} - {type(my_integer)}")

# Float
my_float = 3.14
print(f"Float: {my_float} - {type(my_float)}")

# String
my_string = "Hello, World!"
print(f"String: '{my_string}' - {type(my_string)}")

# Boolean
my_boolean = True
print(f"Boolean: {my_boolean} - {type(my_boolean)}")

# List
my_list = [1, 2, 3, 4, 5]
print(f"List: {my_list} - {type(my_list)}")

# Tuple
my_tuple = (6, 7, 8, 9, 10)
print(f"Tuple: {my_tuple} - {type(my_tuple)}")

# Dictionary
my_dict = {"key1": "value1", "key2": "value2"}
print(f"Dictionary: {my_dict} - {type(my_dict)}")

# Set
my_set = {1, 2, 3, 4, 5}
print(f"Set: {my_set} - {type(my_set)}")

# NoneType
my_none = None
print(f"NoneType: {my_none} - {type(my_none)}")

# Complex
my_complex = 2 + 3j
print(f"Complex: {my_complex} - {type(my_complex)}")
